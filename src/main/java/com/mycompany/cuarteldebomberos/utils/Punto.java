public class Punto {
    private int coordx;
    private int coordy;

    public Punto(int coordx, int coordy) {
        this.coordx = coordx;
        this.coordy = coordy;
    }

    public int getCoordx() {
        return coordx;
    }

    public void setCoordx(int coordx) {
        this.coordx = coordx;
    }

    public int getCoordy() {
        return coordy;
    }

    public void setCoordy(int coordy) {
        this.coordy = coordy;
    }
    
}
